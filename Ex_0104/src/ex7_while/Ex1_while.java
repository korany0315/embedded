package ex7_while;

public class Ex1_while {
	public static void main(String[] args) {
		
		// while : 간단한 구성을 가진 반복문(선 비교, 후 처리)
		// while(){
		// 		조건식이  참일때 실행되는 영역
		// }
		
		int num = 1;
		
		while(num < 5) {
			
			System.out.println(num);
			num++; // 언젠간 멈출 수 있는 코드
			
		} // while
	}//main
}



























